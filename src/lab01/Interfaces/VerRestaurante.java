        Iterator it = ColProd.entrySet().iterator();
        //Iterator itret = ret.entrySet().iterator();
        while(it.hasNext()){
            Map.Entry map = (Map.Entry) it.next();
            modelProd.addElement(map.getKey());
        }
        this.jListProd.setModel(modelProd);
        
        
